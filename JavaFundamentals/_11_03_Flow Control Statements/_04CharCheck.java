package _11_03_FlowControlStatements;

public class _04CharCheck 
{
	public static void main(String[] args) 
	{
	char ch1='z';
	char ch2='e';
	if(ch1>ch2)
		System.out.println(ch2+","+ch1);
	else if(ch1<ch2)
		System.out.println(ch1+","+ch2);
	else
		System.out.println(ch1+","+ch2);
	}
}
